/**
 * UI Test Generator — creates CDP test scripts for published SmartClient plugins.
 *
 * Reads PICT models and generates a test script that:
 *   1. Opens the plugin in the extension browser via CDP
 *   2. Finds the sandbox iframe
 *   3. For each PICT row: fills forms, clicks buttons, verifies grids
 *   4. Reports assertions and screenshots to the dashboard
 *
 * The generated tests exercise the rendered UI, not the raw API.
 * Same PICT models → two test surfaces (API + UI).
 */

import { readFileSync } from 'fs';
import { parsePictParams } from './app-from-pict.mjs';

/**
 * Generate a CDP UI test script for a published plugin.
 *
 * @param {object} options
 * @param {string} options.pluginId - Plugin ID (e.g. "pet-app")
 * @param {string} options.listModelPath - Path to the list endpoint's .pict model
 * @param {string} options.filterFormId - SmartClient ID of the filter form
 * @param {string} options.filterField - Field name in the filter form (e.g. "status")
 * @param {string} options.fetchButtonId - SmartClient ID of the fetch button
 * @param {string} options.gridId - SmartClient ID of the results grid
 * @param {string} [options.createFormId] - SmartClient ID of the create form
 * @param {string} [options.createButtonId] - SmartClient ID of the create button
 * @param {string} [options.importPath] - ScriptClient import path
 * @returns {string} Complete .mjs test script source code
 */
export function generateUiTest(options) {
  const {
    pluginId,
    listModelPath,
    filterFormId = 'filterForm',
    filterField = 'status',
    fetchButtonId = 'btnFetch',
    gridId = 'mainGrid',
    createFormId,
    createButtonId,
    importPath = '../packages/bridge/script-client.mjs',
  } = options;

  // Parse PICT model to get valid filter values
  const modelText = readFileSync(listModelPath, 'utf-8');
  const params = parsePictParams(modelText);
  const filterValues = params.get(filterField) || ['available'];

  const testName = 'test-ui-' + pluginId;
  const totalSteps = filterValues.length + 3; // open + verify + filter tests + screenshot

  return `#!/usr/bin/env node
/**
 * CDP UI Test for plugin: ${pluginId}
 * Generated by api-to-app pipeline (Phase 3: UI tests from PICT models).
 * Tests the rendered SmartClient UI, not the raw API.
 *
 * PICT filter values: ${filterValues.join(', ')}
 */
import { ScriptClient } from '${importPath}';
import http from 'http';
import WebSocket from 'ws';
import fs from 'fs';

const CDP_PORT = 9222;
const PLUGIN_MODE = '${pluginId}';
const TOTAL = ${totalSteps};
const client = new ScriptClient('${testName}', { totalSteps: TOTAL });

async function getTargets() {
  return JSON.parse(await new Promise((res, rej) => {
    http.get('http://localhost:' + CDP_PORT + '/json', r => {
      let d = ''; r.on('data', c => d += c); r.on('end', () => res(d));
    }).on('error', rej);
  }));
}

async function cdpEval(wsUrl, expr, timeout = 15000) {
  const ws = new WebSocket(wsUrl);
  await new Promise(r => ws.once('open', r));
  const result = await new Promise((resolve, reject) => {
    const timer = setTimeout(() => reject(new Error('cdpEval timeout')), timeout);
    const handler = raw => {
      const m = JSON.parse(raw.toString());
      if (m.id === 1) { ws.off('message', handler); clearTimeout(timer); resolve(m); }
    };
    ws.on('message', handler);
    ws.send(JSON.stringify({
      id: 1, method: 'Runtime.evaluate',
      params: { expression: expr, returnByValue: true, awaitPromise: true },
    }));
  });
  ws.close();
  if (result.result?.exceptionDetails) throw new Error(result.result.exceptionDetails.text || 'eval error');
  return result.result?.result?.value;
}

async function cdpScreenshot(wsUrl, path) {
  const ws = new WebSocket(wsUrl);
  await new Promise(r => ws.once('open', r));
  const result = await new Promise(resolve => {
    ws.on('message', raw => { const m = JSON.parse(raw.toString()); if (m.id === 1) resolve(m); });
    ws.send(JSON.stringify({ id: 1, method: 'Page.captureScreenshot', params: { format: 'png' } }));
  });
  ws.close();
  if (result.result?.data) { fs.writeFileSync(path, Buffer.from(result.result.data, 'base64')); return path; }
  return null;
}

async function findPluginTargets(mode) {
  const targets = await getTargets();
  const page = targets.find(t => t.type === 'page' && t.url.includes('mode=' + mode));
  if (!page) return null;
  const idx = targets.indexOf(page);
  for (const t of targets.slice(idx + 1)) {
    if (t.type === 'iframe' && t.url.includes('app.html')) return { page, sandbox: t };
    if (t.type === 'page') break;
  }
  const iframes = targets.filter(t => t.type === 'iframe' && t.url.includes('app.html'));
  if (iframes.length > 0) return { page, sandbox: iframes[iframes.length - 1] };
  return { page, sandbox: null };
}

try {
  await client.connect();
  console.log('UI Test: ' + PLUGIN_MODE);
  console.log('${'='.repeat(50)}\\n');

  // Step 1: Open plugin tab
  await client.progress(1, TOTAL, 'Open plugin');
  let targets = await findPluginTargets(PLUGIN_MODE);
  if (!targets) {
    const all = await getTargets();
    const extId = all.find(t => t.type === 'service_worker')?.url.match(/chrome-extension:\\/\\/([^/]+)/)?.[1];
    if (extId) {
      await new Promise((res, rej) => {
        const req = http.request({ method: 'PUT', hostname: 'localhost', port: CDP_PORT,
          path: '/json/new?' + encodeURI('chrome-extension://' + extId + '/smartclient-app/wrapper.html?mode=' + PLUGIN_MODE) },
          r => { let d = ''; r.on('data', c => d += c); r.on('end', () => res(d)); });
        req.on('error', rej); req.end();
      });
      await new Promise(r => setTimeout(r, 5000));
      targets = await findPluginTargets(PLUGIN_MODE);
    }
  }
  client.assert(targets != null, 'Plugin tab found');
  client.assert(targets?.sandbox != null, 'Sandbox iframe found');
  if (!targets?.sandbox) throw new Error('No sandbox');

  // Step 2: Verify components rendered
  await client.progress(2, TOTAL, 'Verify components');
  let components = null;
  for (let attempt = 0; attempt < 6; attempt++) {
    await new Promise(r => setTimeout(r, 2000));
    targets = await findPluginTargets(PLUGIN_MODE) || targets;
    if (!targets?.sandbox) continue;
    try {
      components = await cdpEval(targets.sandbox.webSocketDebuggerUrl, \`(function() {
        if (typeof isc === 'undefined' || !isc.AutoTest) return null;
        return {
          filterForm: !!isc.AutoTest.getObject('//DynamicForm[ID="${filterFormId}"]'),
          fetchBtn: !!isc.AutoTest.getObject('//Button[ID="${fetchButtonId}"]'),
          grid: !!isc.AutoTest.getObject('//ListGrid[ID="${gridId}"]'),
${createFormId ? `          createForm: !!isc.AutoTest.getObject('//DynamicForm[ID="${createFormId}"]'),` : ''}
${createButtonId ? `          createBtn: !!isc.AutoTest.getObject('//Button[ID="${createButtonId}"]'),` : ''}
        };
      })()\`);
      if (components?.filterForm) break;
    } catch (e) { /* retry */ }
  }
  client.assert(components?.filterForm === true, '${filterFormId} rendered');
  client.assert(components?.fetchBtn === true, '${fetchButtonId} rendered');
  client.assert(components?.grid === true, '${gridId} rendered');

  // Steps 3+: Test each filter value from PICT model
  const filterValues = ${JSON.stringify(filterValues)};
  for (let i = 0; i < filterValues.length; i++) {
    const value = filterValues[i];
    await client.progress(i + 3, TOTAL, 'Filter: ' + value);

    const result = await cdpEval(targets.sandbox.webSocketDebuggerUrl, \`(async function() {
      var form = isc.AutoTest.getObject('//DynamicForm[ID="${filterFormId}"]');
      if (form) form.setValue('${filterField}', '\${value}');
      var btn = isc.AutoTest.getObject('//Button[ID="${fetchButtonId}"]');
      if (btn) btn.click();
      await new Promise(r => setTimeout(r, 5000));
      var grid = isc.AutoTest.getObject('//ListGrid[ID="${gridId}"]');
      var status = isc.AutoTest.getObject('//HTMLFlow[ID="fetchStatus"]');
      return {
        rows: grid ? grid.getTotalRows() : -1,
        status: status ? status.getContents() : '',
      };
    })()\`, 15000);

    const statusText = (result?.status || '').replace(/<[^>]*>/g, '');
    client.assert(result?.rows >= 0, 'Filter ' + value + ': ' + (result?.rows || 0) + ' rows (' + statusText + ')');
    console.log('  ' + value + ': ' + (result?.rows || 0) + ' rows — ' + statusText);
  }

  // Final screenshot
  await client.progress(TOTAL, TOTAL, 'Screenshot');
  const shotPath = '/tmp/${testName}.png';
  await cdpScreenshot(targets.page.webSocketDebuggerUrl, shotPath);
  await client.artifact({ type: 'screenshot', label: 'UI Test: ${pluginId}', filePath: shotPath, contentType: 'image/png' });

  // Close test tab
  try {
    const ws = new WebSocket(targets.page.webSocketDebuggerUrl);
    await new Promise(r => ws.once('open', r));
    ws.send(JSON.stringify({ id: 99, method: 'Page.close' }));
    await new Promise(r => setTimeout(r, 500));
    ws.close();
  } catch (_) {}

  console.log('\\n');
  const exitCode = client.summarize();
  await client.complete({ assertions: client.getAssertionSummary() });
  process.exit(exitCode);

} catch (err) {
  console.error('\\nFatal:', err.message);
  client.assert(false, 'Fatal: ' + err.message);
  client.summarize();
  await client.complete({ assertions: client.getAssertionSummary() }).catch(() => {});
  process.exit(1);
}
`;
}
